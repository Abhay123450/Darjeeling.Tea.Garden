package com.darjeelingteagarden.database

import androidx.room.ColumnInfo
import androidx.room.Entity
import androidx.room.PrimaryKey

@Entity(tableName = "cart")
data class CartEntity(
    @PrimaryKey(autoGenerate = true) val id: Int,
    @ColumnInfo(name = "product_id") val productId: String,
    @ColumnInfo(name = "product_name") val productName: String,
    @ColumnInfo(name = "product_original_price") val originalPrice: Int,
    @ColumnInfo(name = "product_discounted_price") val discountedPrice: Int,
    @ColumnInfo(name = "product_category") val category: String,
    @ColumnInfo(name = "product_grade") val grade: String,
    @ColumnInfo(name = "product_quantity") val quantity: Int
)
